/// Encodes the standard cross-app infrastructure archives common to all iWork
/// packages: `DocumentMetadata`, `Metadata`, `ObjectContainer`,
/// `AnnotationAuthorStorage`, and `DocumentStylesheet`.
///
/// Callers must supply the `document_root_id` that the metadata archives
/// reference back to, and the stylesheet root ID.
pub(crate) struct InfraArchives {
    pub document_metadata: Vec<u8>,
    pub metadata: Vec<u8>,
    pub object_container: Vec<u8>,
    pub annotation_author_storage: Vec<u8>,
    pub document_stylesheet: Vec<u8>,
}

pub(crate) fn encode_infra_archives(
    document_root_id: u64,
    metadata_root_id: u64,
    object_container_root_id: u64,
    document_metadata_root_id: u64,
    annotation_root_id: u64,
    stylesheet_root_id: u64,
) -> Result<InfraArchives, Error> {
    let document_metadata = {
        let body = ProtoMessage::new(vec![
            ProtoField::varint(1, 0),
            ProtoField::bytes(3, Vec::new()),
        ])
        .encode()?;
        let header = synthesize_header_with_references(
            document_metadata_root_id,
            11011,
            body.len(),
            vec![IwaObjectReference {
                object_id: Some(document_root_id),
                kind_hint: Some(3),
                state_hint: Some(1),
            }],
        )?;
        IwaArchive::encode(header, body)?
    };

    let metadata = {
        let body = ProtoMessage::new(vec![
            ProtoField::varint(1, 0),
            ProtoField::bytes(2, object_reference(document_root_id)?),
            ProtoField::bytes(3, object_reference(document_metadata_root_id)?),
            ProtoField::bytes(4, object_reference(object_container_root_id)?),
        ])
        .encode()?;
        let header = synthesize_header_with_references(
            metadata_root_id,
            11006,
            body.len(),
            vec![IwaObjectReference {
                object_id: Some(document_root_id),
                kind_hint: Some(1),
                state_hint: Some(1),
            }],
        )?;
        IwaArchive::encode(header, body)?
    };

    let object_container = {
        let body = ProtoMessage::new(vec![
            ProtoField::varint(1, 0),
            ProtoField::bytes(2, object_reference(document_root_id)?),
        ])
        .encode()?;
        let header = synthesize_header(object_container_root_id, 11008, body.len())?;
        IwaArchive::encode(header, body)?
    };

    let annotation_author_storage = encode_empty_archive(annotation_root_id, 11009)?;

    let document_stylesheet = {
        let body = ProtoMessage::new(vec![
            ProtoField::message(
                2,
                &ProtoMessage::new(vec![
                    ProtoField::string(1, "Normal"),
                    ProtoField::bytes(2, object_reference(stylesheet_root_id)?),
                ]),
            )?,
            ProtoField::message(
                2,
                &ProtoMessage::new(vec![
                    ProtoField::string(1, "Bold"),
                    ProtoField::bytes(2, object_reference(stylesheet_root_id + 1)?),
                    ProtoField::message(11, &ProtoMessage::new(vec![ProtoField::varint(1, 1)]))?,
                ]),
            )?,
        ])
        .encode()?;
        let header = synthesize_header_with_references(
            stylesheet_root_id,
            2,
            body.len(),
            vec![IwaObjectReference {
                object_id: Some(document_root_id),
                kind_hint: Some(1),
                state_hint: Some(0),
            }],
        )?;
        IwaArchive::encode(header, body)?
    };

    Ok(InfraArchives {
        document_metadata,
        metadata,
        object_container,
        annotation_author_storage,
        document_stylesheet,
    })
}
