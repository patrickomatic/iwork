//! Shared IWA archive encoding primitives used by all three app writers.

use crate::iwa::{IwaArchive, IwaArchiveDescriptor, IwaObjectReference, IwaPacket};
use crate::protobuf::{ProtoField, ProtoMessage};
use crate::Error;

/// `MessageInfo.version` triple carried by every real archive header.
pub(crate) const ARCHIVE_VERSION: [u8; 3] = [1, 0, 5];

/// Synthesizes an `IwaPacket` header for a freshly encoded body with no
/// cross-archive object references.
pub(crate) fn synthesize_header(
    root_object_id: u64,
    kind: u64,
    body_len: usize,
) -> Result<IwaPacket, Error> {
    synthesize_header_with_references(root_object_id, kind, body_len, Vec::new())
}

/// Synthesizes an `IwaPacket` header for a freshly encoded body, recording
/// `object_references` so the IWA reader can resolve cross-archive links.
pub(crate) fn synthesize_header_with_references(
    root_object_id: u64,
    kind: u64,
    body_len: usize,
    object_references: Vec<IwaObjectReference>,
) -> Result<IwaPacket, Error> {
    let descriptor = IwaArchiveDescriptor {
        root_object_id: Some(root_object_id),
        kind_hint: Some(kind),
        message_version: Some(ARCHIVE_VERSION.to_vec()),
        body_hint: Some(
            u64::try_from(body_len).map_err(|_| Error::InvalidIwa("body length overflow"))?,
        ),
        object_references,
    };
    Ok(IwaPacket::new(descriptor.encode_message()?.encode()?))
}

/// Encodes a minimal archive that contains only an empty body object of the
/// given type, rooted at `root_object_id`.
pub(crate) fn encode_empty_archive(root_object_id: u64, kind: u64) -> Result<Vec<u8>, Error> {
    let header = synthesize_header(root_object_id, kind, 0)?;
    IwaArchive::encode(header, Vec::new())
}

/// Encodes a protobuf object-reference message: `{ field 1: object_id }`.
///
/// This is the wire format used for cross-object references in iWork archives
/// (both within and across `.iwa` files).
pub(crate) fn object_reference(object_id: u64) -> Result<Vec<u8>, Error> {
    ProtoMessage::new(vec![ProtoField::varint(1, object_id)]).encode()
}

